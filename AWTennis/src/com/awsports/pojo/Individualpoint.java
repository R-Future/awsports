package com.awsports.pojo;

public class Individualpoint extends AwIndividualpoint {
	private Integer lastYear;
	private Integer manSingleEntry;
	private Integer womanSingleEntry;

	public Integer getLastYear() {
		return lastYear;
	}

	public void setLastYear(Integer lastYear) {
		this.lastYear = lastYear;
	}

	public Integer getManSingleEntry() {
		return manSingleEntry;
	}

	public void setManSingleEntry(Integer manSingleEntry) {
		this.manSingleEntry = manSingleEntry;
	}

	public Integer getWomanSingleEntry() {
		return womanSingleEntry;
	}

	public void setWomanSingleEntry(Integer womanSingleEntry) {
		this.womanSingleEntry = womanSingleEntry;
	}
}
