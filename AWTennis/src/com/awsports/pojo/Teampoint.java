package com.awsports.pojo;

public class Teampoint extends AwTeampoint {
	private Integer lastYear;
	private Integer manDoubleEntry;
	private Integer womanDoubleEntry;
	public Integer getLastYear() {
		return lastYear;
	}

	public void setLastYear(Integer lastYear) {
		this.lastYear = lastYear;
	}

	public Integer getManDoubleEntry() {
		return manDoubleEntry;
	}

	public void setManDoubleEntry(Integer manDoubleEntry) {
		this.manDoubleEntry = manDoubleEntry;
	}

	public Integer getWomanDoubleEntry() {
		return womanDoubleEntry;
	}

	public void setWomanDoubleEntry(Integer womanDoubleEntry) {
		this.womanDoubleEntry = womanDoubleEntry;
	}
}	
